#include <stdio.h>

int main() {
    // unsigned integer
    unsigned int ui = 54637U;
    printf("unsigned integer = %d \n", ui);

    // unsigned long integer
    unsigned long int uli = 65757564UL;
    printf("unsigned long integer = %d \n", uli);

    // long int
    long int li = 7685784L;
    printf("long integer = %d \n", li);

    return 0;
}